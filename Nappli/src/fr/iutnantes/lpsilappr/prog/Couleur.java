package fr.iutnantes.lpsilappr.prog;

public enum Couleur {
	
	red ("Rouge"),
	blue ("Bleu"),
	green ("Vert"),
	black ("Noir"),
	white ("Blanc"),
	rose ("Rose");
	
	private String couleur = "";
	
	private Couleur(String couleur) {
		this.couleur = couleur;
	}

	public String toString() {
		return couleur;
	}
}
